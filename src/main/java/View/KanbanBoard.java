    private void updateListViewModel(
            boolean isString,
            ActionEvent evt,
            DefaultListModel list,
            List<Object> tasksDone,
            JList<String> jList) {
        for (Object obj : tasksDone) {
            Task task = (Task) obj;
            if (isString) {
                if (list.contains((String) evt.getSource())) {
                    int index = list.indexOf((String) evt.getSource());
                    list.removeElement((String) evt.getSource());
                    Optional<Object> taskValue = AddTaskDetailsDialog.TASKS_DB.entrySet()
                            .stream()
                            .filter(e -> e.getKey() == index)
                            .map(Map.Entry::getValue).findFirst();
                    if (list.size() == 0)
                        list.addElement(evt.getActionCommand());
                    else
                        list.insertElementAt(evt.getActionCommand(), index);
                    break;
                }
            } else {
                if (!list.contains(task.getTaskName())) {
                    list.addElement(task.getTaskName());
                    break;
                }
            }
        }
        jList.setModel(list);
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        boolean isNumberOfTasksMore =
                TASKS_DB.size() >=
                        Integer.
                                parseInt(jComboBoxNumberOfTasks.getSelectedItem()
                                        .toString());

        if (isNumberOfTasksMore) {
            JOptionPane.showMessageDialog(rootPane,
                    "Cannot add more than defined number of tasks");
            return;
        }

        boolean isString;
        isString = evt.getSource() instanceof String;
        //it means instead of the task being new, it's actually edited
        if (evt.getID() != -2) {
            new AddTaskDetailsDialog(this, rootPaneCheckingEnabled, false).show();
            jTextFieldTotalNumberOfHours
                    .setText(String.valueOf(Task.returnTotalHours()) + "hr(s)");
        }

        List<Object> tasksToDo = AddTaskDetailsDialog.TASKS_DB.entrySet()
                .stream()
                .filter(e -> "To Do"
                        .equals(((Task) e.getValue()).getTaskStatus()))
                .map(Map.Entry::getValue).collect(Collectors.toList());
        List<Object> tasksInProgress = AddTaskDetailsDialog.TASKS_DB.entrySet()
                .stream()
                .filter(e -> "In Progress".equals(((Task) e.getValue()).getTaskStatus()))
                .map(Map.Entry::getValue).collect(Collectors.toList());
        List<Object> tasksDone = AddTaskDetailsDialog.TASKS_DB.entrySet()
                .stream()
                .filter(e -> "Done".equals(((Task) e.getValue()).getTaskStatus()))
                .map(Map.Entry::getValue).collect(Collectors.toList());


        updateListViewModel(isString, evt, toDoModel, tasksToDo, jListTodo);

        updateListViewModel(isString, evt, inProgressModel, tasksInProgress,
                jListInProgress);

        updateListViewModel(isString, evt, doneModel, tasksDone, jListDone);
        jTextFieldTotalNumberOfHours
                .setText(String.valueOf(Task.returnTotalHours()) + "hr(s)");
    }//GEN-LAST:event_jButton1ActionPerformed

