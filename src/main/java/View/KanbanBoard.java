    private void populateAddTaskDetailsWithExistingTask(Task task, String status) {

        dialog.getNameOfTaskTextField().setText(task.getTaskName());
        dialog.getTaskDescriptionTextArea().setText(task.getTaskDescription());
        dialog.getTaskNumberjTextField().setText(task.getTaskNumber());
        dialog.getDeveloperNameTextField().setText(
                task.getTaskDeveloperDetails().getDevFirstName());
        dialog.getDeveloperSurnameTextField().setText(
                task.getTaskDeveloperDetails().getDevLastName());
        dialog.getTaskDurationTextField().setText(
                task.getTaskDuration().toString());
        dialog.getTaskIDTextField().setText(task.getTaskID());
        dialog.getTaskStatusjComboBox().setSelectedItem(status);
    }

    private String statusFromTheBoard(JList<String> list) {
        return list.getName().toLowerCase()
                .contains("todo") ?
                "To Do" : list.getName().toLowerCase()
                .contains("inprogress") ? "In Progress" : "Done";
    }

    private Optional<Object> taskFromTheMapOfTasks(String status,
                                                   JList<String> list) {
        return AddTaskDetailsDialog
                .TASKS_DB.entrySet()
                .stream()
                .filter(e -> status
                        .equals(((Task) e.getValue()).getTaskStatus()) &&
                        list.getSelectedValue()
                                .equals(((Task) e.getValue()).getTaskName()))
                .map(Map.Entry::getValue).findFirst();
    }

    private Integer indexOfItemBeingRemoved(String status,
                                            String selectedValueOnList) {
        return AddTaskDetailsDialog
                .TASKS_DB.entrySet().stream().filter(e -> status
                        .equals(((Task) e.getValue()).getTaskStatus()) &&
                        selectedValueOnList
                                .equals(((Task) e.getValue()).getTaskName()))
                .map(Map.Entry::getKey).findFirst().get();
    }

    private void removeFromMapOfTasks(Task task) {
        AddTaskDetailsDialog.TASKS_DB.entrySet()
                .removeIf(e -> task.getTaskID()
                        .equals(((Task) e.getValue()).getTaskID()));
    }

    private void editExistingTask(Task task) {

        task.setTaskName(dialog.getNameOfTaskTextField().getText());
        task.setTaskDescription(dialog.getTaskDescriptionTextArea().getText());
        task.setTaskNumber(dialog.getTaskNumberjTextField().getText());
        task.setTaskDeveloperDetails(new DeveloperDetails
                (dialog.getDeveloperNameTextField().getText(),
                        dialog.getDeveloperSurnameTextField().getText()));
        task.setTaskDuration(Integer.parseInt
                (dialog.getTaskDurationTextField().getText()));
        task.setTaskID(dialog.getTaskIDTextField().getText());
        task.setTaskStatus(
                dialog.getTaskStatusjComboBox()
                        .getSelectedItem().toString());
    }

    private void moveEditedTaskToTheAppropriateBoard(Task task) {
        switch (dialog.getTaskStatusjComboBox().getSelectedItem().toString()) {
            case "To Do":
                toDoModel.addElement(task.getTaskName());
                jListTodo.setModel(toDoModel);
                break;
            case "Done":
                doneModel.addElement(task.getTaskName());
                jListDone.setModel(doneModel);
                break;
            case "In Progress":
                inProgressModel.addElement(task.getTaskName());
                jListInProgress.setModel(inProgressModel);
                break;
            default:
                break;
        }
    }

    private void modifyTaskEventMouseOnClick(MouseEvent evt) {
        //Get the JList source (To Do, In Progress, Done
        JList<String> list = ((JList<String>) evt.getSource());
        if (list.getSelectedValue() == null)
            return;

        String selectedValueOnList = list.getSelectedValue();

        if (evt.getClickCount() == 1) {
            int index = list.locationToIndex(evt.getPoint());
            if (index != -1) {
                dialog = new AddTaskDetailsDialog(this,
                        rootPaneCheckingEnabled, true);
                String status = statusFromTheBoard(list);

                //Get the list model
                DefaultListModel listModel = (DefaultListModel) list.getModel();
                //We want to search for that task in our database
                Optional<Object> taskFromDB = taskFromTheMapOfTasks(status,
                        list);

                //Once retrieved from the database populate the fields in
                //dialog with the database fetched from the database
                Task task = (Task) taskFromDB.get();
                populateAddTaskDetailsWithExistingTask(task, status);

                //Show the dialog with all the data populated
                dialog.show();
                //Do the same actions as adding a new item in the kanban list
                //except that this time don't open up a new and empty dialog box
                this.jButton1ActionPerformed(new ActionEvent(task.getTaskName(),
                        -2, dialog.getNameOfTaskTextField().getText()));

                if (!dialog.getTaskStatusjComboBox()
                        .getSelectedItem().equals(status)) {
                    int indexOfRemovedItem = indexOfItemBeingRemoved(status,
                            selectedValueOnList);
                    removeFromMapOfTasks(task);
                    editExistingTask(task);

                    //move from old list board, and update the list view
                    listModel.removeElement(task.getTaskName());
                    list.setModel(listModel);

                    moveEditedTaskToTheAppropriateBoard(task);

                    AddTaskDetailsDialog.TASKS_DB.put(indexOfRemovedItem, task);

                } else {
                    int indexOfRemovedItem = indexOfItemBeingRemoved(status,
                            selectedValueOnList);
                    removeFromMapOfTasks(task);
                    editExistingTask(task);

                    AddTaskDetailsDialog.TASKS_DB.put(indexOfRemovedItem, task);
                }
            }
        }
    }

